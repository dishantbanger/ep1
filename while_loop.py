print("while loop ex")
i = 0
while i<10:
    print(i)
    i+=1

#break
print("while loop break ex")
i = 0
while i<10:
    print(i)
    if i ==5:
        break
    i += 1

#continous
print("while loop continous ex")
i = 0
while i<10:
    i += 1
    
    if i ==5:
      continue
    print(i)

#else
print("while loop else ex")
i = 0
while i < 10:
    print(i)
    i+=1
else:
    print ("i is no longer")    


    